CREATE TABLE USERS (
ID_USER INT AUTO_INCREMENT PRIMARY KEY,
FIRST_NAME VARCHAR(50) NOT NULL,
LAST_NAME VARCHAR(50) NOT NULL,
EMAIL VARCHAR(100) UNIQUE NOT NULL,
PHONE_NUMBER VARCHAR(20) UNIQUE NOT NULL,
DISCORD_USERNAME VARCHAR(50) UNIQUE NOT NULL,
UNIVERSITY VARCHAR(100) NOT NULL,
FIELD_OF_STUDY VARCHAR(100) NOT NULL,
ROLE ENUM('PARTICIPANT','MENTOR','STAFF')
);

CREATE TABLE PARTICIPANT(
ID_USER INT PRIMARY KEY,
TEAM VARCHAR(100) NOT NULL,
PROG_LANGUAGES VARCHAR(200) NOT NULL,
MOTIVATION TEXT NOT NULL,
EXPECTATION TEXT NOT NULL,
MAIN_SKILLS VARCHAR(200) NOT NULL,
SKILL_LEVEL ENUM('BEGINNER','INTERMEDIATE','ADVANCED') NOT NULL,
FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
);

CREATE TABLE MENTOR(
ID_USER INT PRIMARY KEY,
YEARS_OF_EXPERIENCE INT NOT NULL,
LINKEDIN VARCHAR(255),
PORTFOLIO VARCHAR(255) NOT NULL,
AREA_OF_EXPERTISE VARCHAR(200) NOT NULL,
TECHNOLOGIES VARCHAR(200) NOT NULL,
MENTORED_BEFORE BOOLEAN DEFAULT 0,
FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
);

CREATE TABLE STAFF (
ID_USER INT PRIMARY KEY,
PREFERRED_ROLE ENUM('TECHNICAL_SUPPORT','LOGISTICS','COMMUNICATION','ORGANIZATION') NOT NULL,
ORGANIZED_BEFORE BOOLEAN DEFAULT 0,
FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
);


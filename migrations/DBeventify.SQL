CREATE TRIGGER check_participant_role
BEFORE INSERT ON PARTICIPANT
FOR EACH ROW
BEGIN

    -- verifier que le ROLE dans USERS est PARTICIPANT
    IF (SELECT ROLE FROM USERS WHERE ID_USER = NEW.ID_USER) <> 'PARTICIPANT' THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'User role is not PARTICIPANT';
    END IF;

    -- verifier que l'utilisateur n'existe pas dans d'autres roles
    IF EXISTS (SELECT 1 FROM MENTOR WHERE ID_USER = NEW.ID_USER)
       OR EXISTS (SELECT 1 FROM STAFF WHERE ID_USER = NEW.ID_USER) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'User already has another role';
    END IF;

END //

